/**
 * Clase con un metodo para multisumar 3 valores flotantes
 * @author nicolasgonzalezmartinez
 */
public class MultSuma
{
    /**
     * Metodo encargado de multisumar 3 valores flotantes
     * @param a Valor double
     * @param b Valor double
     * @param c Valor double
     */
    public void multsuma(double a , double b , double c)
    {
        System.out.println(a*b+c);
    }
}
